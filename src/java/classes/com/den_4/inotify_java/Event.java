package com.den_4.inotify_java;

import java.util.EventObject;

/**
 * Represents all events generated by inotify-java.
 * 
 * @since 2.1 Added this base class common to {@link InotifyEvent} and
 * {@link EventQueueFull}
 */
abstract class BaseEvent extends EventObject {
    private static final long serialVersionUID = 3665027631470495686L;

    /**
     * Creates a base event associated with a descriptor.
     * 
     * @param descriptor Watch or file descriptor
     * @see NativeInotify#addWatch(String, int)
     * @see NativeInotify#getFileDescriptor()
     */
    public BaseEvent(Integer descriptor) {
        super(descriptor);
    }

    /**
     * Returns the descriptor associated with this event. This descriptor can be
     * either a watch descriptor or a file descriptor. It is left to subclasses
     * to indicate which is used. {@inheritDoc}
     * 
     * @return Integer descriptor
     * @see NativeInotify#addWatch(String, int)
     * @see NativeInotify#getFileDescriptor()
     */
    @Override
    public Integer getSource() {
        return (Integer) super.getSource();
    }

}
